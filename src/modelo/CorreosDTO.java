package modelo;

/**
 *
 * @author Exe
 */
public class CorreosDTO {
    
    
    private String promotor, ejecutivo, informativo, exceptuado, texto, gho,
            seguridad, precedencia, fecha;
    private int idCorreo;

    public String getPromotor() {
        return promotor;
    }

    public void setPromotor(String promotor) {
        this.promotor = promotor;
    }

    public String getEjecutivo() {
        return ejecutivo;
    }

    public void setEjecutivo(String ejecutivo) {
        this.ejecutivo = ejecutivo;
    }

    public String getInformativo() {
        return informativo;
    }

    public void setInformativo(String informativo) {
        this.informativo = informativo;
    }

    public String getExceptuado() {
        return exceptuado;
    }

    public void setExceptuado(String exceptuado) {
        this.exceptuado = exceptuado;
    }

    public String getTexto() {
        return texto;
    }

    public void setTexto(String texto) {
        this.texto = texto;
    }

    public String getGho() {
        return gho;
    }

    public void setGho(String gho) {
        this.gho = gho;
    }

    public String getSeguridad() {
        return seguridad;
    }

    public void setSeguridad(String seguridad) {
        this.seguridad = seguridad;
    }

    public String getPrecedencia() {
        return precedencia;
    }

    public void setPrecedencia(String precedencia) {
        this.precedencia = precedencia;
    }

    public int getIdCorreo() {
        return idCorreo;
    }

    public void setIdCorreo(int idCorreo) {
        this.idCorreo = idCorreo;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }


}